<p><strong>1. why rice?</strong><br>

   I chose Theatre Rice because I was sick of everyone questioning the
authenticity of my Asian-American roots. I mean, the Jewish people are
totally originally from Asia. I hate living in exile!!!!!!</p>
  

<p><strong>2. why you?</strong><br>

   I was an accident. I just recently found out that my mom tested me to see
if I was retarded in the womb. If so, the doctors may have recommended
abortion. Doesn't that suck? So the answer to "why me" is really: cuz I was
accidentally conceived and ended up not retarded (...or so they
think...joke's on them... suckers!).</p>


<p><strong>3. life outside of rice?  what do you do?</strong><br>

   I like compupers. And sax. And piano. And sleep. I used to be big in the
Jewish community. Now I'm just average-sized.</p>
 

<p> <strong>4. Britney or xtina?  why?</strong><br>

   While "Not a Girl, Not Yet a Woman" has its existential moments, I would
have to say the empowering messages of self-confidence and independence
conveyed in "Beautiful" and "The Voice Within" take the cake. Proverbially,
that is.</p>
 

<p> <strong>5. elaborate on the meaning of life.</strong><br>

   My mom always says all we can do in life is live each moment with the
best morals and ethics we can conjure up, and search for happiness wherever
we are. She is so lucky I wasn't aborted - who else would believe that shit?</p>
 

<p> <strong>6. GPA?  as in Greatest Personal Achievement?</strong><br>

   I would say my existence is my greatest achievement. Just think of all
the sperm and eggs that never fulfilled that impossible chance of joining to
become a baby. And me? I'm the biggest baby of them all!</p>

 

<p> <strong>7. Most embarassing moment?  ever?</strong><br>

   In kindergarten we made cookies for our final presentation and I was
piling up the cookies on my plate and the teacher came and grabbed my arm
and yelled at me. I didn't know! I was a "little" fat kid! So I cried and
blamed it on the fact that my mom couldn't come that day. But I knew I was
really crying over lost dessert. I hope my teacher is dead. And in heaven.
Eating lots of cookies that are just way too sweet and she gets a cavity,
but ultimately enjoys her ethereal eternity with an all-loving God who
values dramatic irony and sweets.</p>
 

<p> <strong>8. who would you never kill?</strong><br>

   I mean I wouldn't kill her, I just assumed that she'd be dead by now. Not
because of the whole cookie thing, just because that's the way of life. On
second thought, I really hope she didn't just choke on a cookie while
reading this. I'd feel hella bad. </p>
 

<p> <strong>9. (bonus) favorite lord of the rings character and why?</strong><br>

   I think the ring did an awesome performance. I mean he just fit so
perfectly on all the dudes' fingers. Um... and the Lord. He was very
convincing as the ultimate supreme being of the universe... Shit I gotta go
see that movie.</p>
 

            
