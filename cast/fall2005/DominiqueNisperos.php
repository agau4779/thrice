<p><strong>1. why rice?</strong><br>

   Because every plan for world domination has its first step, life in 
college wouldn't be right without rice, and I'm going for a record here. </p>
  

<p><strong>2. why you?</strong><br>

   Because "I'm fuckin GREAT!" And because I was elected producer and have 
to fulfill my end of the bargain. Also, I have a lot of heart and get things 
done. </p>


<p><strong>3. life outside of rice?  what do you do?</strong><br>

   I enjoy riding BART, getting lost while taking walks, lying on memorial 
glade, collecting sea glass, meandering with headphones, and doing other 
things that help me pretend that I don't have a 7 other things that I should 
be doing , am behind on reading in every class, and need to wash the dishes!</p>
 

<p> <strong>4. Britney or xtina?  why?</strong><br>

   As a feminist and a scholar I would have to choose Clarkson. I don't why 
I like her singing "Since You've Been Gone" really loud in the shower. 
HAHA, But for reals FOR REALS I would choose Karen O. "Waaaaiit, they don't 
love you like I love you", how can you beat that?</p>
 

<p> <strong>5. elaborate on the meaning of life.</strong><br>

   I think the meaning of life is to do good, for others and for yourself. 
Common says in one of his songs that god is love. I think this is true. 
Maybe it isn't a singular bearded being sitting on a chair above everyone 
else, but I think that there is a greater collective good, that is beyond 
the comprehension of an individual. I think that unless life is being made 
better for an individual and a collective, than there is no purpose.</p>
 

<p> <strong>6. GPA?  as in Greatest Personal Achievement?</strong><br>

   GPA? Hmmm, my greatest personal achievement, going to college I think. 
That has a lot of smaller accomplishments tied up in it. One of which would 
be my summer research project. Yes, giving my final presentation was 
awesome. A life long accomplishment of mine is thinking critically and 
wanting to learn.</p>

 

