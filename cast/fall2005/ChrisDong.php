<p><strong>1. why rice?</strong><br>

   Theatre Rice has been running through my veins since my Freshman year in 
Fall 2001 and I wouldn't have it any other way, even post-graduation (yes, I 
am an Alum). I love the passion and love that everyone in Theatre Rice has 
for the art and for one another.</p>
  

<p><strong>2. why you?</strong><br>

   Theatre Rice used to be my girlfriend and now I am back, begging for her 
forgiveness.</p>


<p><strong>3. life outside of rice?  what do you do?</strong><br>

   I cook and go to yummy restaurants. I like musicals and watching movies. 
I tend to sing a lot (albeit very poorly) and know the lyrics to really 
random songs. I am also actively looking for a full-time job, so if you know 
of anything let me know!</p>
 

<p> <strong>4. Britney or xtina?  why?</strong><br>

   Neither. They both scare me. Although one of my roommates at Disney World 
was obsessed with Britney, so if I had to choose, it would be Britney. But 
it wouldn't be for her looks or her music. </p>
 

<p> <strong>5. elaborate on the meaning of life.</strong><br>

   The meaning of life is to positively touch as many people's lives as you 
can. Proactive gestures are just as important as inactive ones, like trying 
to avoid killing puffins, baby penguins, puppies, and kittens (although I am 
allergic to cats…)</p>
 

<p> <strong>6. GPA?  as in Greatest Personal Achievement?</strong><br>

   Spending seven great semesters in Theatre Rice and helping create and 
produce some of the best shows UC Berkeley has seen.</p>

 

<p> <strong>7. Most embarassing moment?  ever?</strong><br>

   When I was in first grade my best friend and I were running around the 
playground and we decided to run onto the grassy area, even though it had 
just rained moments before. My elementary school put large red flags in the 
grass when it had just rained so that everyone would know to stay off the 
grass until it dried up. We ran toward the grass and as soon as I stepped 
onto the grass I slipped and fell backward, landing flat on my back. All of 
my clothes were covered in mud and I had to go to the principal's office. My 
Mom had to bring me a change of clothes and I had to explain to her why I 
was all muddy. This was only one of two times I was sent to the principal's 
office. </p>
 

<p> <strong>8. who would you never kill?</strong><br>

   That's confidential.</p>
 

<p> <strong>9. (bonus) favorite lord of the rings character and why?</strong><br>

   I have never seen a Lord of the Rings film nor have I ever read any of 
the books. And I plan to keep it that way for a while, until someone drugs 
me, kidnaps me, and then forces me to watch all three films. Although the 
name Frodo rings a bell.</p>
 

            
