<p><strong>1. why rice?</strong><br>

   Rice is a safe environment to risk failure. It saved me from my own
mundane slow death into the silence of the night. It breathed a new
sense of wonder and revived my pursuit of the arts. It was one of the
best things that I chose in life. I have fond memories of CAT, Purple
Spirals, performing and supporting new and old friends.</p>
  

<p><strong>2. why you?</strong><br>

   I've met some of the most interesting, loyal, and life long love/
rooommate/friends from Rice. I want to give back, inspire, and spark
the love of acting, passion, and "moments" of sheer raw humanity. I
hope to make the world at least a little better--if anything, make
some one's life breathe a little easier.</p>


<p><strong>3. life outside of rice?  what do you do?</strong><br>

   I am a bachelor candidate for English, emphasis on Literary Theory.
(my bs major) But in truth, I am pursuing independent studies in
experimental/ traditional film media, painting, photography, and
entertainment design. I listen to acid jazz, post rock, alternative,
punk, and instrumentals. I am a connoisseur of tea, mind you. I rock
climb and deperately want my wilderness first responder certificate. I
surf but only in the summertime because it gets really cold any time
else. I like comic books, superheroes, and anime. I don't do it as
often as I like, but I love to travel. My next destinations: Japan,
Australia, New Zealand, and Italy.</p>
 

<p> <strong>4. Britney or xtina?  why?</strong><br>

   Who is Britney? What is x-tina? I guess none. I don't know who/what they are.</p>
 

<p> <strong>5. elaborate on the meaning of life.</strong><br>

   The meaning of life? I vaguely remember someone saying something
simple and it makes sense to me. 1) Try to be happy 2) Don't hurt
anyone in the process 3) Hope you find someone to love<br><br>

I really don't know what the meaning of life is. Sometimes it is a
series of absurd concidences, sometimes a universal organic movement
towards some determined path. All I know is that we will all die
someday. I think individuals must attribute their own meaning to life
through choice of actions. I choose to live with the intent to make
mistakes, kick, wail, scream, jump, shout, love, and revel in the
burst of my existence.</p>
 

<p> <strong>6. GPA?  as in Greatest Personal Achievement?</strong><br>

   GPA? Very clever. So far, not getting kicked out of Berkeley is nice.
But on a metaphysical level:<br><br>

To laugh everyday and go to sleep content everynight.</p>

 

<p> <strong>7. Most embarassing moment?  ever?</strong><br>

   I know I have many. If you were to blackmail me, I'm sure you
could. I can't think of one good one right now.</p>
 

<p> <strong>8. who would you never kill?</strong><br>

   My family, friends, and the love of my life.</p>
 

