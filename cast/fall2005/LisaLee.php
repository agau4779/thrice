<p><strong>1. why rice?</strong><br>

   you meet a lot of people that don't like this or that, for example, "raw fish?!
gross" or "oriental food?! i hate oriental food!" - but i peronally have never met someone, or
could imagine that there's someone out there who might say, "what?! rice?! i HATE rice! rice is not 
discriminating. it loves everyone and everyone loves it.</p>
  

<p><strong>2. why you?</strong><br>

   why me what? why me - because i am the last pink hippo alive on this planet, and theatre rice is doing all it can to preserve my
fat dancing twitch.</p>


<p><strong>3. life outside of rice?  what do you do?</strong><br>

   i seem to do a lot of my sleeping during classes. if not, you can find me at
various rehearsals, moffitt/evans/tolman, or at KALX 90.7 FM. very rarely though, if i am
lucky, you might find me curled up in front of the t.v. with my huge white blanket and a cup of hot
tea. those "healing" moments are very necessary.</p>
 

<p> <strong>4. Britney or xtina?  why?</strong><br>

   i don't know what the latter one is (did you type xena wrong?) so i'll have to go
with britney. despite the ever-so-odd songs that (wait, can we even qualify them as songs?) she
sings, or not, y'all have to agree with me that she's hell of a dancer. i wish i could be as just
as sexy when i dance hehe.</p>
 

<p> <strong>5. elaborate on the meaning of life.</strong><br>

   the meaning of life - join theatre rice! ok no seriously, who are you not to be?
instead of asking "why," ask "why not?" see the bigger picture, do what YOU want, go with your
instincts, worry about the little stuff, but worry about the big stuff more.</p>
 

<p> <strong>6. GPA?  as in Greatest Personal Achievement?</strong><br>

   my greatest personal achievement has got to be FORTE. i don't think i've ever
loved dancing so much. other than that, it's just "getting by." we're put down by so much crap every
single day that to be able to get out of bed and face the world is a freaking accomplishment.</p>

 

<p> <strong>7. Most embarassing moment?  ever?</strong><br>

   oh i have many. i jumped into a crap hole (literally) when i was young. i
could've drowned in it but a voice told me to swim through the crap and emerge so i can live, so i did.
hey man, i was young and i thought it was mud ok? what else? oh, thinking that a boy likes you when
he was just being "friendly" haha. i think those are the best because you end up feeling like
such a fool!</p>
 

<p> <strong>8. who would you never kill?</strong><br>

   i would kill a lot of people. don't get me started.</p>
 

<p> <strong>9. (bonus) favorite lord of the rings character and why?</strong><br>

   you know, i've only seen the first LORD OF THE RINGS and i don't even remember
all of their names. i like the character with the arrows. elves are hot.</p>
 

            
