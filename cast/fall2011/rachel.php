<h4>Name:</h4>
Rachel Kang

<h4>Year & Major:</h4>
Freshman, Rhetoric

<h4>What attracted you to join Theatre Rice? What makes you stay in it?</h4>
I heard about Theatre Rice my sophomore year in high school when I visited Berkeley, and the idea of an Asian American performance group fascinated me because, well, in high school I was the only Asian that did theatre! It was nice to see that there were others who are just as passionate about theatre as I am. I love that this group helps dispel stereotypes about Asian Americans and shows audiences that we belong on the stage as much as everyone else :) 

<h4>Favorite Fruit?</h4>
Apricots and peaches. 

<h4>Hobbies? Special talents?</h4>
I can do a mean banana dance. 

<h4>Movies or TV shows that you enjoy?</h4>
My favorite movies are The Emperor's New Groove and Fight Club. My favorite TV shows include Hey Arnold, The Office, It's Always Sunny in Philadelphia, and Recess. 

<h4>Music genres or artists you like?</h4>
Not to sound like a hipster, but I really enjoy listening to folk indie and classic rock. I never get tired of '90s pop or showtunes either. I love Queen, Metric, Bloc Party, and the Yeah Yeah Yeahs.

