<h4>What attracted you to join Theatre Rice? What makes you stay in it?</h4>
I've always been a big fan of rice, and I just thought, if I liked eating rice so much, why not watch rice too? And better yet, in a the-a-ter! But really though, I like the freedom of creativity that Theater Rice offers to not only its members but also the audiences that watch it as well. Very rarely do you see a troupe that not only possess so much unifying creative expression, but also be able to display and incorporate individual creative talents as well. It's just pure entertainment nirvana. 

<h4>Favorite Fruit?</h4>
