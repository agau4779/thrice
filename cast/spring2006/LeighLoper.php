<p><strong>1. Why are you doing this?</strong><br>
   I wanted to do something new this semester, get connected with some like minded people. I started Tae Kwon Do fall semester, which is cool, but it's not exactly the place to scream and roll on the ground. Well, I guess it is. But Theatre Rice is a different kind of yelling and rolling on the ground...yeah this answer is going downhill really quickly so I'm just going to move on... </p>

<p><strong>2. What makes you special?</strong><br>
   I a special because I have no brain-to-mouth filter, I just go for it. This can lead to some problems, yes, but also some funny moments. Also, I make a damn delicious tiramisu, which doesn't hurt either.</p>

<p><strong>3. Theatre Rice. Let's break it down, shall we? How do you
feel about theatre?</strong><br>
   Theatre. Love it. Especially since I've had so many starring roles. That's a lie since the only thing I can think of is beinga flying monkey in my middle school's production of Wizard of Oz. I'm pretty sure that that doesn't count. I don't know, theatre is everywhere in the ridiculous moments of everyday life if you just open you eyes.</p>

<p> <strong>4. And Rice?</strong><br>
   Well rice is white, so that's nice I guess. Other than that, it's delicious! and goes with everything. Really, what would eating be without rice? Pretty freaking awful, that's right.</p>

<p> <strong>5. Alright, be honest, how often do you check the
facebook?</strong><br>
   Hmmm...more like how often do i NOT check the facebook... I'm not going to lie: my facebook addiction is a little embarassing.</p>

<p> <strong>6. What is/was your favorite cartoon? why?</strong><br>
   There is no beating the Ninja Turtles. I would belt that theme song out and dance around in the living room wearing my Ninja Turtle pajamas and then go play with my Ninja Turtle action figures in the Turtle Blimp. I dug it, big time. Oh, I had a Ninja Turles beach towel too, just so I could keep representing even when I went to the pool.</p>

<p> <strong>7. What would you do for a million dollars?</strong><br>
   Is this question trying to make me write a list of degrading activities? That's sick. I don't know, there is a lot I would do and a lot I wouldn't do for a million dollars.</p>

<p> <strong>8. What would you pay a million dollars to see other people
do?</strong><br>
   I would pay a million dollars to see people do something really ridicuoulous. Maybe I could design my own really twisted version of Survivior and go from there... </p>

<p> <strong>9. Mirror mirror on the wall, who's the diva-est of them
all?</strong><br>
   There are a lot of divas out there. My personal divas include all of my teachers, dance instructors, mentors, and coaches over the years. Even more than that, my friends are some crazy diva batches who bring the A game. </p>

