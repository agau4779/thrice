<p><strong>2. What makes you special?</strong><br>
  How boringly uninteresting I am. Also, I can move things with my mind.</p>

<p><strong>3. Theatre Rice. Let's break it down, shall we? How do you
feel about theatre?</strong><br>
  My feelings would best be described in a monologue: Look, I love you. And I've tried to tell you so many times, but you keep sending me mixed signals. Everything was fine until I noticed the wedding ring. Is THAT why you never called me? Enough with the head games, baby. I'm moving on. Because there has got to be a nice girl out there for me who will love me and respect me and bake me little delicious pastries with powdered sugar every once in a while, who will understand that every now and then I like to listen to one good sad song and cry because I'm emo like that, who will let me choose what we watch on tv every now and then, for instance, if Walker Texas Ranger and Jeapordy are on at the same time and I go with secret option three "Matlock rerun," that would be okay and she would not judge me. There's just gotta be that girl out there. There's just gotta be.</p>

<p> <strong>4. And Rice?</strong><br>
  Delicious. Happiness and serving size are directly proportional.</p>

<p> <strong>5. Alright, be honest, how often do you check the
facebook?</strong><br>
  I check facebook as much as I blink. You could make a drinking game out of me. Everyone's a winner!</p>

<p> <strong>6. What is/was your favorite cartoon? why?</strong><br>
  Old school Teenage Mutant Ninja Turtles. Hands down! I mean, come on! They're named after artists, they kick butt, AND they eat pizza! Cowabunga, my friends.</p>

<p> <strong>7. What would you do for a million dollars?</strong><br>
