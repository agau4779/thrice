<p><strong>1. Why are you doing this?</strong><br>
Theatre, positive social change, representation of Asian Americans by Asian Americans, the community, the opportunity to lead meetings :) and work with Ex-Com, meeting and working with amazing people.  Plus, I <3 Jon Yu.  And when I write "Plus," I mean "Actually, it's all because".</p> 

<p><strong>2. What makes you special?</strong><br>
I am interested in the role of gender in identity and relationship, in the power of the printed word and the spoken word, in the life of theatre, in the beauty of selflessness and graciousness.  I love waking up to music, the sunshine waving through the window blinds, my one hour of television per week in Project Runway, my family, making/writing/buying/receiving greeting cards, talking to myself, laughing til your face looks scary, talking with my roommate in "roight" voices, Princess Pat.  I'm not as fond of watermelon as most people I know are.  I shy away from parties and bake and play board games with my friends instead.  I don't know; isn't the fact that I'm the only person that has experienced all of my experiences enough?  Goodness, what more do you want from me, Jon Yu?  My heart, and now this?</p> 

<p><strong>3. Theatre Rice.  Let's break it down, shall we?  How do you
feel about theatre?</strong><br>
I love theatre because I think it's magical... it's a way to connect both to yourself and to the people around you - and having one relationship fuel the other, acting on your impulses, doing something truthfully, breathing in the present while creating something that will only exist in that moment, can only be talked about in retrospect.  Theatre is alive, and only your fellow artists and the audience members get to be a part of that life.   And with Theatre Rice, we make a positive difference in our community and, by extension, in our world.  </p>

<p> <strong>4. And Rice?</strong><br> 
Do you know, I almost always make it too moist.  My roommate has acquired a taste for mushy rice.  (Haha!  MPCT!)  Oh, but I see your capital 'R' and I raise you this: I'm enjoying this semester so much.  I thank the cast and Ex-Com and the legacy of Rice and old Ricers for this.  I am continually impressed by people's abilities to express the truth of the moment, to think up amazing stuff that I wouldn't have thought of, to work for improvement, to share with one another, to laugh and be laughed at or with, ... We're all part of the tapestry that is Rice. :)</p> 

<p> <strong>5. Alright, be honest, how often do you check the 
facebook?</strong><br>
I don't even want to know.  But I still heart facebook, because - more importantly than being an effective tool for both procrastination and stalking - how else would I get to date Jon Yu?</p> 

<p> <strong>6. What is/was your favorite cartoon?  why?</strong><br>
Hey Arnold!  I love the Christmas episode.  Helga loves Arnold so much, and he doesn't even know all that she does and feels for him!  Pepper Ann is also great - or at any rate, the opening sequence and song are (and yes, her last name is pretty awesome, too).</p> 

<p> <strong>7. What would you do for a million dollars?</strong><br>
Something that warranted it.  Like write something amazing or invent something useful... or circle numbers with a pencil...</p> 

<p> <strong>8. What would you pay a million dollars to see other people
do?</strong><br>
Save someone.</p>

<p> <strong>9. Mirror mirror on the wall, who's the diva-est of them 
all?</strong><br>
Funny how Jon Yu works for so many of these answers.  </p>

<p> <strong>10. Ask an open-ended question.</strong><br>
What are the metaphysical and philosophical implications of this question not have a question mark.</p>
 

   
   
            
