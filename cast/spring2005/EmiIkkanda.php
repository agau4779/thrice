<p><strong>1) Who are you?</strong><br>
Some say the human body is mostly water so ... I am mostly water. The other parts of me are part artist, part writer, part movie freak, part wuss rock fan, part liberal democrat, part atheist, part shower singer, part girl, not yet a woman, partially over 21, part los angeles, part san francisco, partial to small furry animals. </p>


<p><strong>2) Why are you in Theatre Rice?</strong><br>
I said "I want to join" and they said "Okay." Actually ... I had decided to try out for Rice because I was always (and continue to be) shy, and this was to be an experiment with myself. Oh, that doesn't sound right, does it? </p>

<p><strong>3) What are you searching for?</strong><br>
A way to compromise idealism with reality, with politics, with human relationships, etc. I'm searching for my own little corner of the universe ... I'm searching for myself, and I have a suspicion that I'm in Paris. </p>

<p> <strong>4) What other extra-curricular activities are you involved in? </strong><br>
Uuuugh. Uuuugh. Other extra-curricular activities? Free time? What? </p>

<p> <strong>5) What do you look for in an ideal mate?</strong><br>
 Cute Asian young woman seeks a fine twenty something man with great sense of humor and personality. Enjoys long conversations, walks down the beach, and foreign films. Non-smoker, a plus. If you are this person, call me at 555-5555. </p>

<p> <strong>6) Why are you in your major? </strong><br>
Cavemen drew on walls, and so did I - with traditional waxen crayons. Mother did not approve of the murals, and suggested I try commiting art to paper. Emi did so, and continued doing so, both with pictures and words, and hasn't been able to stop. Hence, Emi majors in English and Art. </p>

<p> <strong>7) What do you do in your free time?</strong><br>
Free time? HAHAHAHAHAHAHA. Oh, you're serious? On good days, I paint, I draw, I write stories or plays, I watch films obsessively, I stop by poetry slams and galleries and concerts, I try to party but not too crazily, I watch the Daily Show when I can, um ... I think about Theatre Rice morning, noon, and night ... </p>

