Wow. I suppose recently I've been listening to Badly Drawn Boy, Eels, Spoon, Guster, Franz Ferdinand, Postal Service, Coldplay, for some alt rock. I most recently saw Modest Mouse, Dido, Howie Day ... Um. </p>

