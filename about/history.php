			<p>
				Theatre Rice, the first modern Asian American theater group at the University of California, Berkeley, was started in the fall of 1998. Since its conception, Theatre Rice has grown considerably, both artistically and physically. In the past several years, the group has begun to attract an increasingly larger audience and a greater membership. Theatre Rice has emerged from a relatively unknown group to a group whose performances have become an anticipated community event.
			</p>
			<p>
				Theatre Rice was conceived to provide people (particularly, but not exclusively, Asian American) who might not otherwise have a chance to participate in theatrical arts the opportunity to act, write, sing, direct, dance, and learn behind-the-stage techniques. What sets Theatre Rice apart from other dramatic groups is its open and unintimidating environment, and the basic ethic that effort and commitment are more important than experience or innate talent. Because the group is comprised of both seasoned and fledgling actors, there exists a healthy dynamic within which members can share their experiences and learn from each other.
			</p>
			<p>
				The Asian American aspect of Theatre Rice is important because it is a step towards combating the misrepresentation, as well as the lack of representation, of the Asian American community in popular culture. By its mere existence, Theatre Rice is fulfilling its purpose of putting forth ideas that stem from the minds of Asian Americans, and therefore addressing the Asian American population as well as the greater community within Berkeley and beyond. 
