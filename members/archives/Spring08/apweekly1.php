<b>7. "Satellites" by Diana Son by the Aurora Theatre Company (An
official Theatre Rice outing on Saturday February 16th</b><b>)<br>
</b>Theatre Rice alum, Charles Lee invites us to:<br>
<p class="narrow"> <strong><em>Satellites</em></strong><br>
by Diana Son <br>
directed by Kent Nicholson<br>
January 25 - March 2, 2008 </p>
<p>Diana Son (<em>Stop Kiss</em>,
"Law &amp; Order: Criminal Intent") raises challenging questions
about
parenting, racial identity, and community in this Off-Broadway hit.
Called " an engrossing, unpredictable, and believable portrait" by <em>Newsday</em>,
<em>Satellites</em> finds humor and fulfillment in the most
unexpected places.</p>
<p><em>Satellites</em> is being produced as the
fully-staged production in the <a
 href="http://www.auroratheatre.org/show.php?prod_id=56&amp;ref=seas">Global
Age Project (GAP)</a>,
an Aurora Theatre Company initiative that encourages playwrights and
directors to explore life in the 21st century and beyond. Four new
works dealing with global age concerns will be read in the GAP Monday
night play reading series in tandem with <em>Satellites</em>.
</p>
