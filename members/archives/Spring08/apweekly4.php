<div>Work the charm. I know it may be hard sometimes to flier on
Sproul, this is my fourth semester and still I'll get weak knees and
butterflies. No worries. I've tested out techniques so here are some
tips: </div>
<div>&nbsp;</div>
<div></div>
<div>-<u>Always get people you know first.</u> It
boosts your own
confidence and you don't insult an acquitance (it's a win-win). People
you know will almost always pick up your flier, 99% of the time. </div>
<div>-<u>Be confident.</u> How will people know your
show's good if you
don't have faith in your show? Look them in the eyes, stand up
straight, but be receiving! Has a success rate of 87%. </div>
<div>-<u>Be patient.</u> Instead of mumbling Theatre
Rice and trying to
be "done with it." Work the charm, spend a little bit more time. 63.3%
success rate </div>
<div>-<u>Smile.</u> Research has been done that the
smiling puts people at ease. 43% success rate </div>
<div>-<u>Mention your performing.</u> Instead of
being some hack who's
there for money or obligatory tabling. You're there because you're
performing. Performers are attractive. Let them know, I've noticed
people will pick up your flier 30% of the time just because you mention
you are going to be in the show. </div>
<div></div>
<div>Also all these techniques work if you were scoping at a bar.
</div>
